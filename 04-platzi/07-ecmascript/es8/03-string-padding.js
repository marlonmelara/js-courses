const string = "Hello";

// Usa padStart para expandir la longitud de 'string' a 15 caracteres,
// rellenando con "_hi" al inicio hasta alcanzar esa longitud.
console.log(string.padStart(15, "_hi"));

// Usa padEnd para expandir la longitud de 'string' a 15 caracteres,
// rellenando con "_hi" al final hasta alcanzar esa longitud.
console.log(string.padEnd(15, "_hi"));
