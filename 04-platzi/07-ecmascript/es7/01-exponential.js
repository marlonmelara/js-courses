/**
 * Operador de Exponenciación (**)
 * - Definición:
 *   El operador de exponenciación (**) es una forma rápida de calcular la potencia de un número.
 *   El formato es: base ** exponente, donde:
 *   - base es el número que queremos elevar.
 *   - exponente es el número de veces que la base se multiplica por sí misma.
 *
 * Ejemplo:
 *
 */

// Usando el operador de exponenciación para calcular 3 elevado a la potencia de 4.
// En este caso, 3 es la base y 4 es el exponente.
const data = 3 ** 4;

// Imprimiendo en consola el resultado de 3 elevado a la potencia de 4, que es 81.
console.log(data); // Output: 81
